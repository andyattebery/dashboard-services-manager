    [Test]
    public void MixedProviderIds_EachReplacesItsOwn()
    {
        var existing = new List<Service>
        {
            new Service("OldTraefik", "https://old-t.example", "c", null, null, "nas-01", false, autogenerated: true, providerId: "docker-01-traefik"),
