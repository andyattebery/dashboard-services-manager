    [Test]
    public void UserAddedExisting_WithMatchingTitle_IsKept()
    {
        var existing = new List<Service>
        {
            new Service("Jellyfin", "https://jellyfin.user", "media", null, null, "user-host", false, autogenerated: false),
        };
        var incoming = new List<Service>
        {
            new Service("Jellyfin", "https://jellyfin.auto", "media", null, null, "auto-host", false, autogenerated: true),
        };

