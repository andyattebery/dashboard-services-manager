using Dsm.Managers.Services;
using Dsm.Shared.Models;

namespace Dsm.Managers.Tests.UnitTests;

[CancelAfter(TestTimeouts.HungThresholdMs)]
public class ServicesCombinerTests
{
    private readonly ServicesCombiner _servicesCombiner = new ServicesCombiner();

    [Test]
    public void AutogeneratedExisting_SameName_DifferentHostname_BothSurvive()
    {
        var existing = new List<Service>
        {
            new Service("Jellyfin", "https://jellyfin.old", "media", null, null, "old-host", false, autogenerated: true),
        };
        var incoming = new List<Service>
        {
            new Service("Jellyfin", "https://jellyfin.new", "media", null, null, "new-host", false, autogenerated: true),
        };

        var result = _servicesCombiner.CombineExistingServicesWithNewServices(existing, incoming);

        Assert.That(result, Has.Count.EqualTo(2));
        Assert.That(result.Select(s => s.Url),
            Is.EquivalentTo(new[] { "https://jellyfin.old", "https://jellyfin.new" }));
    }

    [Test]
    public void AutogeneratedExisting_SameName_SameHostname_IsReplaced()
    {
        var existing = new List<Service>
        {
            new Service("Jellyfin", "https://jellyfin.old", "media", null, null, "host-1", false, autogenerated: true),
        };
        var incoming = new List<Service>
        {
            new Service("Jellyfin", "https://jellyfin.new", "media", null, null, "host-1", false, autogenerated: true),
        };

        var result = _servicesCombiner.CombineExistingServicesWithNewServices(existing, incoming);

        Assert.That(result, Has.Count.EqualTo(1));
        Assert.That(result[0].Url, Is.EqualTo("https://jellyfin.new"));
    }

    [Test]
    public void AutogeneratedExisting_NullHostname_FallsBackToNameMatch()
    {
        var existing = new List<Service>
        {
            new Service("Jellyfin", "https://jellyfin.old", "media", null, null, null, false, autogenerated: true),
        };
        var incoming = new List<Service>
        {
            new Service("Jellyfin", "https://jellyfin.new", "media", null, null, null, false, autogenerated: true),
        };

        var result = _servicesCombiner.CombineExistingServicesWithNewServices(existing, incoming);

        Assert.That(result, Has.Count.EqualTo(1));
        Assert.That(result[0].Url, Is.EqualTo("https://jellyfin.new"));
    }

    [Test]
    public void AutogeneratedExisting_WithMatchingHostname_IsDropped()
    {
        var existing = new List<Service>
        {
            new Service("Old", "https://old.example", "media", null, null, "host-1", false, autogenerated: true),
        };
        var incoming = new List<Service>
        {
            new Service("New", "https://new.example", "media", null, null, "host-1", false, autogenerated: true),
        };

        var result = _servicesCombiner.CombineExistingServicesWithNewServices(existing, incoming);

        Assert.That(result.Select(s => s.Name), Is.EquivalentTo(new[] { "New" }));
    }

    [Test]
    public void AutogeneratedExisting_WithNoOverlap_IsKept()
    {
        var existing = new List<Service>
        {
            new Service("Existing", "https://existing.example", "media", null, null, "host-a", false, autogenerated: true),
