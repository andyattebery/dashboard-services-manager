    [Test]
    public void UserAddedExisting_WithMatchingHostname_IsKept()
    {
        var existing = new List<Service>
        {
            new Service("CustomTool", "https://custom.example", "utilities", null, null, "host-1", false, autogenerated: false),
        };
        var incoming = new List<Service>
        {
            new Service("Other", "https://other.example", "utilities", null, null, "host-1", false, autogenerated: true),
        };

        var result = _servicesCombiner.CombineExistingServicesWithNewServices(existing, incoming);

        Assert.That(result.Select(s => s.Name), Is.EquivalentTo(new[] { "CustomTool", "Other" }));
    }

    [Test]
    public void Result_IsSortedByCategoryThenName()
    {
        var incoming = new List<Service>
        {
            new Service("Zeta", "https://z.example", "media", null, null, "h", false, autogenerated: true),
            new Service("Alpha", "https://a.example", "network", null, null, "h", false, autogenerated: true),
            new Service("Beta", "https://b.example", "media", null, null, "h", false, autogenerated: true),
        };

        var result = _servicesCombiner.CombineExistingServicesWithNewServices(new List<Service>(), incoming);

        Assert.That(result.Select(s => (s.Category, s.Name)), Is.EqualTo(new[]
        {
            ("media", "Beta"),
            ("media", "Zeta"),
            ("network", "Alpha"),
        }));
    }

    [Test]
    public void EmptyNewServices_ReturnsAllExisting()
    {
        var existing = new List<Service>
        {
            new Service("A", "https://a.example", "c", null, null, "h", false, autogenerated: true),
            new Service("B", "https://b.example", "c", null, null, "h", false, autogenerated: false),
        };

        var result = _servicesCombiner.CombineExistingServicesWithNewServices(existing, new List<Service>());

        Assert.That(result.Select(s => s.Name), Is.EquivalentTo(new[] { "A", "B" }));
    }

