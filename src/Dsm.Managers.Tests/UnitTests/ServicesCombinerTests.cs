            new Service("NewTraefik", "https://new-t.example", "c", null, null, "nas-01", false, autogenerated: true, providerId: "docker-01-traefik"),
        };

        var result = _servicesCombiner.CombineExistingServicesWithNewServices(existing, incoming);

        Assert.That(result.Select(s => s.Name), Is.EquivalentTo(new[] { "NewTraefik", "OldDocker" }));
    }

    [Test]
    public void SameNameAndHostname_DifferentProviderId_DedupKeepsIncoming()
    {
        var existing = new List<Service>
        {
            new Service("Scrutiny", "https://scrutiny.docker", "infra", null, null, "nas-01", false, autogenerated: true, providerId: "nas-01-docker"),
        };
        var incoming = new List<Service>
        {
            new Service("Scrutiny", "https://scrutiny.traefik", "infra", null, null, "nas-01", false, autogenerated: true, providerId: "docker-01-traefik"),
        };

        var result = _servicesCombiner.CombineExistingServicesWithNewServices(existing, incoming);

        Assert.That(result, Has.Count.EqualTo(1));
        Assert.That(result[0].Url, Is.EqualTo("https://scrutiny.traefik"));
        Assert.That(result[0].ProviderId, Is.EqualTo("docker-01-traefik"));
    }

